    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        // Mail Authen
        final String auth_host = "mail.is.viettel.com.vn";
        final String auth_port = "465";


//        props.put("mail.smtp.host", auth_host);
//        props.put("mail.smtp.socketFactory.port", auth_port);
//        props.put("mail.smtp.socketFactory.class", "javax.net.ssl.SSLSocketFactory");
//        props.put("mail.smtp.auth", "true");
//        props.put("mail.smtp.port", auth_port);

        props.setProperty("mail.smtp.host", auth_host);
        props.setProperty("mail.smtp.post", auth_port);
        props.setProperty("mail.smtp.starttls.enable", "true");
        props.setProperty("mail.smtp.auth", "true");
        props.setProperty("mail.smtp.socketFactory.class", "javax.net.SSLSocketFactory");
        props.setProperty("mail.smtp.socketFactory.fallback", "false");
        
        auth_email = jTextField1.getText();
        
        try {
            Session mailSession = Session.getInstance(props,
                    new javax.mail.Authenticator() {
                        protected PasswordAuthentication
                        getPasswordAuthentication() {
                            return new PasswordAuthentication(auth_email, auth_password);
                        }
                    });

            Message message = new MimeMessage(mailSession);

            message.setFrom(new InternetAddress(auth_email)); // From

            /**
             * * Recipient **
             */
            message.setRecipients(Message.RecipientType.TO,
                    InternetAddress.parse(jTextField2.getText())); // To
            message.setSubject(jTextField5.getText());

            // create the message part 
            MimeBodyPart messageBodyPart = new MimeBodyPart();

            String Attach = jTextField6.getText(); // File Path

            //fill message
            messageBodyPart.setText(jTextArea1.getText());

            Multipart multipart = new MimeMultipart();
            multipart.addBodyPart(messageBodyPart);

            // Part two is attachment
            messageBodyPart = new MimeBodyPart();
            DataSource source = new FileDataSource(Attach);
            messageBodyPart.setDataHandler(new DataHandler(source));

            String fileName = Attach.substring(Attach.lastIndexOf('\\') + 1, Attach.length());
            messageBodyPart.setFileName(fileName);
            multipart.addBodyPart(messageBodyPart);

            message.setContent(multipart);

            Transport.send(message);

            JOptionPane.showMessageDialog(null, "Mail Send Successfully.");

        } catch (MessagingException e) {
            throw new RuntimeException(e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

